package com.educore.staff;

/**
 * StaffPermission — كل الصلاحيات المتاحة للموظفين.
 *
 * المدرس (TEACHER) والأدمن (ADMIN) عندهم كل الصلاحيات تلقائياً.
 * الموظف (STAFF) بيحصل بس على الصلاحيات اللي اختارها المدرس له.
 *
 * الصلاحيات بتتعرض كـ checkboxes في لوحة التحكم.
 */
public enum StaffPermission {

    // ── الطلاب ──────────────────────────────────────
    /** قبول وتفعيل حسابات الطلاب الجديدة */
    APPROVE_STUDENTS("قبول الطلاب"),

    /** رفض أو حجب حسابات الطلاب */
    REJECT_STUDENTS("رفض / حجب الطلاب"),

    /** عرض بيانات وقائمة الطلاب */
    VIEW_STUDENTS("عرض بيانات الطلاب"),

    // ── المحتوى التعليمي ──────────────────────────
    /** إضافة وتعديل وحذف الكورسات والسيشنز والويكس */
    MANAGE_COURSES("إدارة الكورسات والمحتوى"),

    /** إضافة وتعديل الكويزات والتكليفات */
    MANAGE_QUIZZES("إدارة الكويزات والتكليفات"),

    /** إدارة بنك الأسئلة والمواضيع */
    MANAGE_QUESTION_BANK("إدارة بنك الأسئلة"),

    // ── الحضور والكارنيهات ───────────────────────
    /** تسجيل الحضور عبر QR أو يدوياً */
    MANAGE_ATTENDANCE("تسجيل الحضور"),

    /** إصدار وتفعيل كارنيهات الطلاب */
    ISSUE_CARDS("إصدار الكارنيهات"),

    // ── المالية ──────────────────────────────────
    /** شحن محافظ الطلاب */
    TOP_UP_WALLET("شحن محافظ الطلاب"),

    /** إنشاء وتعديل وإلغاء كوبونات الخصم */
    MANAGE_COUPONS("إدارة الكوبونات"),

    // ── التسجيل ──────────────────────────────────
    /** إضافة / تمديد / إلغاء تسجيلات الطلاب يدوياً */
    MANAGE_ENROLLMENTS("إدارة تسجيلات الطلاب"),

    // ── الإشعارات ────────────────────────────────
    /** إرسال إشعارات للطلاب */
    SEND_NOTIFICATIONS("إرسال الإشعارات"),

    // ── التقارير ─────────────────────────────────
    /** عرض لوحة التقارير والإحصائيات */
    VIEW_ANALYTICS("عرض التقارير والإحصائيات");

    /** الاسم العربي — بيتعرض في الـ UI */
    public final String arabicLabel;

    StaffPermission(String arabicLabel) {
        this.arabicLabel = arabicLabel;
    }
}
